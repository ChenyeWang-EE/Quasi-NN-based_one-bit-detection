function y = statechangeoutput(hISI)
    y = zeros(8,8);
    y(1,7) = hISI(1)*exp(1j*pi*(-0.25-0.5))+hISI(2)*exp(1j*pi*(0-0.25));
    y(1,8) = hISI(1)*exp(1j*pi*(0.25-0.5))+hISI(2)*exp(1j*pi*(0-0.25));
    y(2,3) = hISI(1)*exp(1j*pi*(-0.25+0.5))+hISI(2)*exp(1j*pi*(0+0.25));
    y(2,4) = hISI(1)*exp(1j*pi*(0.25+0.5))+hISI(2)*exp(1j*pi*(0+0.25));
    y(3,1) = hISI(1)*exp(1j*pi*(0-0.25))+hISI(2)*exp(1j*pi*(0.5-0.25));
    y(3,2) = hISI(1)*exp(1j*pi*(0+0.25))+hISI(2)*exp(1j*pi*(0.5-0.25));
    y(4,5) = hISI(1)*exp(1j*pi*(1-0.25))+hISI(2)*exp(1j*pi*(0.5+0.25));
    y(4,6) = hISI(1)*exp(1j*pi*(1+0.25))+hISI(2)*exp(1j*pi*(0.5+0.25));
    y(5,3) = hISI(1)*exp(1j*pi*(0.5-0.25))+hISI(2)*exp(1j*pi*(1-0.25));
    y(5,4) = hISI(1)*exp(1j*pi*(0.5+0.25))+hISI(2)*exp(1j*pi*(1-0.25));
    y(6,7) = hISI(1)*exp(1j*pi*(-0.25-0.5))+hISI(2)*exp(1j*pi*(1+0.25));
    y(6,8) = hISI(1)*exp(1j*pi*(0.25-0.5))+hISI(2)*exp(1j*pi*(1+0.25));
    y(7,5) = hISI(1)*exp(1j*pi*(1-0.25))+hISI(2)*exp(1j*pi*(-0.5-0.25));
    y(7,6) = hISI(1)*exp(1j*pi*(1+0.25))+hISI(2)*exp(1j*pi*(-0.5-0.25));
    y(8,1) = hISI(1)*exp(1j*pi*(0-0.25))+hISI(2)*exp(1j*pi*(-0.5+0.25));
    y(8,2) = hISI(1)*exp(1j*pi*(0+0.25))+hISI(2)*exp(1j*pi*(-0.5+0.25));
end
